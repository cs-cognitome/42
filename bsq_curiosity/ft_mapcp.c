#include "libft.h"

char *ft_mapcp(char *dest, char *src)
{
	int i;
	int n;

	n = 0;
	i = 0;
	while (src[i - 1] != '\n')
		i++;
	while(src[i])
	{
		dest[n] = src[i];
		i++;
		n++;
	}
	dest[i] = '\0';
	return (dest);
}
