#include "libft.h"

char *ft_strcp(char *dest, char *src)
{
	int i;

	i = 0;
	while(src[i] && src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
