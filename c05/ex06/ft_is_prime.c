int		ft_is_prime(int nb)
{
	long i;

	i = 3;
	if (nb < 2)
		return (0);
	if (nb == 2)
		return (1);
	if (!(nb % 2))
		return (0);
	while (i * i <= nb)
	{
		if (!(nb % i))
			return (0);
		i += 2;
	}
	return (1);
}
